import numpy as np

x = np.random.randint(30 , size = (5,6))
y = np.random.randint(30 , size = (5,6))

#add them two arrays
z = np.add(x, y)

print(z)

#subtract them two arrays
z = np.subtract(x, y)

print(z)

#multiply them two arrays
z = np.multiply(x, y)

print(z)

#divide them two arrays
z = np.divide(x, y)

print(z)